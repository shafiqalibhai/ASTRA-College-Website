<dt>b. In Germany</dt>
Worked with the Institut fuer Massivbau, University of Stuttgart  as a fellow of the German Academic Exchange Service (DAAD) on deputation. The  research program was preceded by an intensive German language course at a  Geothe Institut in Germany,  which imparted good proficiency in the language at an advanced (Mittelstufe)  level.<br />
The  studies at the University   of Stuttgart included  advanced techniques, such as the methods of incremental launching and free  cantilever construction, analysis and design of concrete structures with  particular reference to box girder bridges, and advanced analysis using several  software packages, besides developing computer programs for analysis and design  of concrete structures.<br />
Visited  several construction sites and manufacturing units to learn about the  techniques adopted in Europe. Prepared  research reports in German on the analysis of box girders subjected to  distortional loading, box girders without support diaphragms, analysis of  concrete members under combined bending, shear and torsion.<br />
A few  papers based on these investigations were brought out in various journals, such  as Beton- und Stahlbetonbau (in German), the Journal of the American Concrete  Institute, Concrete International (ACI), and the Indian Concrete Journal. The  stay in Germany  also provided an opportunity to attend several international seminars and  conferences, and to interact with leading engineers and experts of industry.<br />
Visited Germany under the re-invitation programme of the  DAAD to work with the Institute fuer Leichtbau Entwerfen und Konstruieren  (Institute for Lightweight Structural Conceptual Design and Construction), Stuttgart University (1. November  30 December  2002) for two months. The visit was  intended to study the current developments in structural design and  construction.
<dt>c. In Australia</dt>
Worked for doctorate degree with the Department of Civil  and Agricultural Engineering, University   of Melbourne. The  research project dealt with thermal stresses in concrete bridges due to  insolation effects. These stresses, not usually considered in the design of  bridges, have led to distress in several structures.<br />
The  investigations have led to the development of design temperature distributions  for concrete bridges of various configurations for the ambient data in Melbourne, Australia  and in New Delhi, India. Comprehensive studies on the  influence of various parameters on thermal stresses were carried out, and the  stresses under the temperature distributions recommended by various codes of  practice were compared. Variation of thermal stresses in bridges curved in  plan, and in non-prismatic structures was also reported. The effects of creep  on thermal stresses were investigated, and a critical review of literature on  various aspects of thermal stresses was presented. A comprehensive document on  the major computer programs developed on these aspects was also prepared.<br />
<br />
<b>Thesis title : </b>Design temperature distributions and thermal  stresses in concrete bridges<br />
<br />
These investigations were broad  based, and were published in the proceedings of international conferences and  journals of various countries, such as Australia,  India, Libya, Singapore  and the U. S. A.<br />
Apart  from the research program, the stay with the University of Melbourne  provided an opportunity to gain teaching experience as well. The subjects  handled included Mechanics of Solids, Concrete Structures, Computer  Programming, Steel Structures, and Introduction to Engineering. These courses  were taught at the Departments of Civil and Agricultural Engineering, and  Architecture and Building Science of the University of Melbourne,  and at the Department of Civil and Aeronautical Engineering of the Royal  Melbourne Institute of Technology. <br />
The stay provided rich experience  through interaction with various professionals and experts, and through helping  other graduate students.<br />
Attended  several seminars and conferences, and presented papers on the research  investigations completed from time to time. Visited several construction sites  and manufacturing units to gain insight into the new developments in  construction industry, besides conducting site visits for the students of the  University.<br />
