<h3>EXPERIENCE</h3><br /><dt>a. In India</dt>
Worked for about fourteen years with the Structural  Engineering Research Centre, Roorkee,   India, a  premier organisation of research in structural engineering. The  responsibilities as a scientist involved mainly participation in various  research and design projects, and providing consultancy services and training  facilities to other organisations and engineering students. Other  responsibilities included working on the committees of the Indian Standards  Institution, and the Indian Roads Congress for drafting various codes of  practice.<br />
The  projects participated included various aspects of bridge structures,  prestressed concrete structures, composite structures, non-destructive testing,  testing of beam models, studies on bridge loadings, live load studies on  bridges, and preparation of design aids and computer software.<br />
During  the stay of about fourteen years with the Structural Engineering Research  Centre, published more than 40 research papers, 14 research reports and one  handbook on design tables for concrete bridge deck slabs, attended several  conferences and seminars, and contributed discussions on several publications.<br />
Worked  with the Birla Institute of Technology and Science, Pilani, and later with V. R. Siddhartha Engineering College,  Vijayawada, and Chaitanya Bharathi Institute of Technology, Hyderabad after returning to India in 1988.<br />
Presently  working with the University College of Engineering (Autonomous), Hyderabad. In addition,  conducted post-graduate classes for the Jawaharlal  Nehru Technological  University, Hyderabad,  and for the Regional Engineering College,  Warangal (off-campus centre at Hyderabad).<br />
The  responsibilities include teaching structural analysis and design, CAD, Theory  of Elasticity, Plasticity, Plates and Shells, and the FEM to under-graduate and  post-graduate students, supervising the research projects at under-graduate,  post-graduate and doctoral levels, providing consultancy services,  participating on various committees, and carrying out research on various  aspects of civil engineering. Recorded  more than 30 lectures on strength of materials and theory of structures.<br />
Other  activities include developing course materials and courses, and interaction  with other organisations such as Engineering Staff College of India, National  Council of Cement and Building Materials and National Academy of Construction in  training professional engineers. <br />
Supervised  several post-graduate and under-graduate projects. Conducting several site  visits, and developed computer programs for the use and training of students.  Prepared lecture notes for various courses on structural analysis and design.<br />
Published  a handbook on bridge deck slabs, a professional book on detailing of  reinforcement, a monograph on durability of concrete structures, and four  textbooks on structural analysis and strength of materials for under- and post-graduate  students.<br />
Several  under- and post-graduate students, who were supervised, published technical  papers in professional journals and conferences in India and overseas. Presently  supervising seven doctoral candidates.<br />
An active  consulting engineer, and a member, and chairman of enquiry and advisory  committees of Municipal Corporation, Hyderabad, High Court, Andhra Pradesh, and  Institution of Engineers (India), AP State Centre, and member, State Dam Safety  Inspection Committee.
