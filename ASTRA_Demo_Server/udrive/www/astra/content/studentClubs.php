  <dl><h3>STUDENT CLUBS</h3><dd>
The Aurora ethos believes that true education can be accomplished not through imposition but through aspiration. Nothing can accomplish this better than club activities that are ‘by the students, of the students and for the students’.</dd>
<dt> LITERARY CLUB</dt><dd>
The literary club organizes activities like debate, elocution, essay writing, and general quiz, during the academic year. Competitions are organized on special days like Engineers’ Day, August 15 and the Annual Day. Students with talent and inclination are motivated to participate. The club also provides a forum for developing communication skills, and cultivate a creative outlook towards their profession.</dd>

<dt> CULTURAL CLUB</dt><dd>
Under the umbrella of the cultural club, students are encouraged to organize dramatics, music, painting, singing and dance competitions, etc. These activities tap the creativity of students and go a long way in making them successful as creative professionals. Such activities hone their personalities and allow them  to be in sync with other aspects of their being.</dd>

<dt>TECHNOLOGY CLUB</dt><dd>
This club is a reflection of new ideas and technologies. It shares the latest information on emerging trends, events and personalities involved in designing and working of technologies.</dd>

<dt>PROFESSIONAL CLUBS</dt><dd>
In order to encourage involvement in technical activities and enhance professional competence among budding engineers, the college has instituted a number of student chapters of national and international professional bodies. Some of these are: Institution of Electrical and Electronics Engineers (IEEE), Institution of Electronics and Telecommunication Engineers (IETE),  Instrument Society of India (ISOI), Computer Society of India (CSI), Association for Computing Machinery (ACM) and the Indian Society for Technical Education (ISTE). </dd>
<dd>
These student chapters host expert sessions, arrange talks and workshops which help students in acquiring knowledge and skills on advanced technical subjects. All engineering students are strongly advised to seek membership in technical bodies for academic and professional growth.</dd>
