			  <dt>About IEEE</dt>
				<dd>The IEEE, a non-profit organization, is the world&#39;s leading 
				professional association for the advancement of technology. The 
				full name of the IEEE is the Institute of Electrical and Electronics 
				Engineers, Inc., although the organization is referred to by the 
				letters I-E-E-E and pronounced Eye-triple-E. </dd>
				<dt>Who the IEEE Serves</dt>
				<dd>Through its global membership, the IEEE is a leading authority 
				on areas ranging from aerospace systems, computers and telecommunications 
				to biomedical engineering, electric power and consumer electronics 
				among others. Members rely on the IEEE as a source of technical 
				and professional information, resources and services. To foster 
				an interest in the engineering profession, the IEEE also serves 
				student members in colleges and universities around the world. Other 
				important constituencies include prospective members and organizations 
				that purchase IEEE products and participate in conferences or other 
				IEEE programs.</dd>
				<dt>Quick Facts </dt>
				<dd>The IEEE has*: </dd>
