				80,000 students, in over 160 countries. </dd>
				<dd> 319 sections in ten geographic 
				regions worldwide. </dd>
				<dd> 1676 chapters that unite local members with 
				similar technical interests. </dd>
				<dd> more than 1,526 student branches 
				at colleges and universities in 80 countries. </dd>
				<dd> 39 societies and 
				5 technical councils representing the wide range of technical interests. 
				</dd>
				<dd> 132 transactions, journals and magazines. </dd>
