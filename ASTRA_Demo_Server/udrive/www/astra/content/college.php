<h3>THE COLLEGE</h3>
<dl>
<dt>INTRODUCTION</dt><dd>
The College is located at Bandlaguda,13 kilometers from Koti and 10 kilometers from Mehdipatanam. The college has an area of 25 Acres. The college offers B.Tech. programmes in Computer Science and Engineering, Information Technology, Electronics and Communication Engineering, Electrical and Electronics Engineering and Civil Engineering. It also offers M.Tech. programmes in Computer Science and Engineering, Information Technology, Electronics and Communication Engineering and Very Large Scale Integration (VLSI) System Design.</dd><dd>

Aurora prepares not just students with engineering degrees in their hands, but people with personality different from other students. It is this reason that Aurora has become a brand to be trusted among students looking for quality education.</dd>

<dt>THE GOAL</dt><dd>
In seeking to fulfill its comprehensive mission, Aurora pursues three principal institutional goals: meaningful research, effective teaching, and service to society.  Contribution towards the realization of these goals essentially constitutes the standard by which members of the academic staff are evaluated.  </dd>

<dt>RESEARCH</dt><dd>
The college acknowledges that the preservation and expansion of knowledge through scholarly inquiry are functions that distinguish institutions of higher learning. The institution believes that scholarly inquiry promotes effective teaching, besides being a service to society.  Aurora, therefore, seeks to preserve knowledge in its archives and libraries; employs teaching faculty holding research degrees awarded by reputed institutions of advanced education; honours those who achieve distinction as scholars; maintains laboratories, research centers, and numerous administrative entities that function to promote the expansion of knowledge.</dd>

<dt>TEACHING</dt><dd>
Aurora is committed to the transmission of knowledge. The institution’s primary responsibility is to its student clientele, and, in this regard, effective classroom teaching is Aurora’s most pervasive medium for the dissemination of the results of its faculty’s scholarly endeavors.  The central concern of the institution is, therefore, excellence in those instructional activities that provide students with opportunities for a comprehensive education and a specialized professional training. The institution assigns substantial weight to teaching in its process of faculty evaluation, recognizing that excellence requires not only knowledge on the part of a teacher but a continuing quest for knowledge, a constant review of curricula and methods, flexibility and creativity in the classroom, and an unceasing effort to individualize instruction. Towards this end, Aurora seeks to measure the quality of instruction through both student and peer evaluation, and regularly subject its academic programs to external review by accrediting agencies.</dd>

<dt>TRAINING</dt><dd>
Aurora is an educational institution striving to utilize the services of its highly qualified men and women, whose collective  expertise encompasses virtually every field of human endeavor, for the benefit of the community.  Aurora reaches out to serve the society of which it is a part.</dd>

<dt>TIMINGS</dt><dd>
The college functions 6 days a week, from 9:10 AM to 4:20 PM, with a lunch break of 30 minutes, from 12:30 to 01:00 PM.</dd>
